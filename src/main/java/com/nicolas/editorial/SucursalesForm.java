    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (txtDomicilio.getText().trim().equalsIgnoreCase("") || 
                txtTelefono.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Complete TODOS los campos", 
                    "ADVERTENCIA", JOptionPane.WARNING_MESSAGE); 
        }else {
            try {
                String telefono, domicilio, dni_emp;
                int n_registro;
                telefono = txtTelefono.getText();
                domicilio = txtDomicilio.getText();
                Empleado emp = (Empleado) cboEmpleado.getSelectedItem();
                dni_emp = emp.getDni();
                Revista rev = (Revista) cboRevista.getSelectedItem();
                n_registro = rev.getN_registro();
                Sucursal suc = new Sucursal(telefono, domicilio, dni_emp, n_registro);
                impl.add(suc);
                
                JOptionPane.showMessageDialog(null, "Sucursal registrada", 
                    "INFO", JOptionPane.INFORMATION_MESSAGE); 
                
                mostrarSucursales();
                limpiar();
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), 
                    "ERROR", JOptionPane.ERROR_MESSAGE); 
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

