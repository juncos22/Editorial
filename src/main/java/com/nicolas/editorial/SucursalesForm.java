    private void mostrarSucursales(){
        try {
            impl = new SucursalImpl();
            List<Sucursal> sucursales = impl.getAll();
            TmSucursal tm = new TmSucursal(sucursales);
            tblSucursales.setModel(tm); 
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", 
                    JOptionPane.ERROR_MESSAGE); 
        }
    }
    
    private void cargarEmpleados(){
        try {
            impl2 = new EmpleadoImpl();
            List<Empleado> empleados = impl2.getAll();
            
            cboEmpleado.removeAllItems();
            
            for (Empleado e : empleados) {
                cboEmpleado.addItem(e); 
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", 
                    JOptionPane.ERROR_MESSAGE); 
        }
    }
    
    private void cargarRevistas(){
        try {
            impl3 = new RevistaImpl();
            cboRevista.removeAllItems();
            
            for (Revista rev : impl3.getAll()) {
                cboRevista.addItem(rev); 
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", 
                    JOptionPane.ERROR_MESSAGE); 
        }
    }
    
    private void limpiar(){
        txtTelefono.setText("");
        txtDomicilio.setText("");
        cboEmpleado.setSelectedIndex(0);
        cboRevista.setSelectedIndex(0); 
        
        txtTelefono.requestFocus();
    }
